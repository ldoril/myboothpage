function getNode({ url, title, image, description }) {
  return `
      <div class="section">
        <div class="title">
          <a href="${url}" target="_blank">${title}</a>
        </div>

        <div class="image">
          <img
            width="300"
            src="${image}"
          />
        </div>

        <div class="dec">
          ${textLengthOverCut(description, 60, "...")}
        </div>
      </div>`;
}

function textLengthOverCut(txt, len, lastTxt) {
  if (len == "" || len == null) {
    // 기본값
    len = 20;
  }
  if (lastTxt == "" || lastTxt == null) {
    // 기본값
    lastTxt = "...";
  }
  if (txt.length > len) {
    txt = txt.substr(0, len) + lastTxt;
  }
  return txt;
}

